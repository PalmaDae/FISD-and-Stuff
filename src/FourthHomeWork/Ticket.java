package FourthHomeWork;

public class Ticket {
    public String name;
    public int price;
    public String description;

    public Ticket(String name, int price, String description) {
        this.name = name;
        this.price = price;
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public String getDescription() {
        return description;
    }
}
